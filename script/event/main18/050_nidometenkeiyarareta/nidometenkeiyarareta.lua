    CH("AAKEN")
  })
  WINDOW:DrawFace(272, 16, SymAct("AAKEN"), FACE_TYPE.DECIDE)
  WINDOW:Talk(SymAct("AAKEN"), 2122848079)
  WINDOW:CloseMessage()
