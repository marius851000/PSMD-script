function hanyou_tsuginoasamyuu01_start()
  WINDOW:Narration(TimeSec(0.3), TimeSec(0.3), 1854354219)
  WINDOW:CloseMessage()
  CAMERA:SetEye(SymCam("CAMERA_00"))
  CAMERA:SetTgt(SymCam("CAMERA_00"))
  CH("HERO"):SetDir(RotateTarget(-45))
  CH("MYUU"):SetDir(RotateTarget(45))
  CH("HERO"):SetMotion(SymMot("EV001_SLEEP01"), LOOP.ON, TimeSec(0))
  CH("MYUU"):SetMotion(SymMot("SB001_SLEEP01"), LOOP.ON, TimeSec(0))
  SCREEN_A:FadeIn(TimeSec(0.5), true)
  local taskMyuu00 = function()
    CH("MYUU"):SetMotion(SymMot("SB001_SLEEP02"), LOOP.OFF)
    CH("MYUU"):WaitPlayMotion()
    CH("MYUU"):SetMotion(SymMot("SB001_YAWN"), LOOP.OFF)
    CH("MYUU"):WaitPlayMotion()
    CH("MYUU"):SetMotion(SymMot("WAIT02"), LOOP.ON)
    CH("MYUU"):DirTo(CH("HERO"), Speed(350), ROT_TYPE.NEAR)
  end
  TASK:Regist(taskMyuu00)
  TASK:Sleep(TimeSec(0.5))
  CH("HERO"):SetMotion(SymMot("EV001_SLEEP02"), LOOP.OFF)
  CH("HERO"):WaitPlayMotion()
  CH("HERO"):DirTo(CH("MYUU"), Speed(350), ROT_TYPE.NEAR)
  CH("HERO"):WaitRotate()
  TASK:WaitTask()
  TASK:Sleep(TimeSec(0.3))
  CH("MYUU"):SetFacialMotion(FACIAL_MOTION.HAPPY)
  CH("MYUU"):SetManpu("MP_SPREE_LP")
  TASK:Regist(subEveDoubleJump, {
    CH("MYUU")
  })
  WINDOW:DrawFace(20, 88, SymAct("MYUU"), FACE_TYPE.HAPPY)
  WINDOW:Talk(SymAct("MYUU"), 2006712938)
  WINDOW:Talk(SymAct("MYUU"), 1555124649)
  TASK:WaitTask()
  WINDOW:CloseMessage()
  CH("MYUU"):ResetManpu()
  CH("MYUU"):ResetFacialMotion()
  SCREEN_A:FadeOut(TimeSec(0.5), true)
end
