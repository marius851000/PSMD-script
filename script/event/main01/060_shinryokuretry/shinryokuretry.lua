function main01_shinryokuretry01_start()
  CAMERA:SetSisaAzimuthDifferenceVolume(Volume(2))
  CAMERA:SetSisaRateVolume(Volume(1))
  subEveFromProgFadeSet()
  WINDOW:DrawFace(20, 88, SymAct("KONOHANA"), FACE_TYPE.PAIN)
  WINDOW:Talk(SymAct("KONOHANA"), 935381619)
  WINDOW:CloseMessage()
  WINDOW:KeyWait()
  SCREEN_A:FadeOutAll(TimeSec(0.5), true)
  WINDOW:ForceCloseMessage()
  SCREEN_A:FadeInAll(TimeSec(0), true)
  CAMERA:SetEye(SymCam("CAMERA_00"))
  CAMERA:SetTgt(SymCam("CAMERA_00"))
  CH("HERO"):SetMotion(SymMot("EV001_DOWN01"), LOOP.ON, TimeSec(0))
  CH("KONOHANA"):SetMotion(SymMot("EV001_DOWN01"), LOOP.ON, TimeSec(0))
  local taskHeroDownUp = function()
    CH("HERO"):SetMotion(SymMot("EV001_DOWN02"), LOOP.OFF)
    CH("HERO"):WaitPlayMotion()
    CH("HERO"):ResetFacialMotion()
    subEveLookAround(CH("HERO"), Speed(600))
    CH("HERO"):DirTo(CH("KONOHANA"), Speed(500), ROT_TYPE.NEAR)
  end
  local taskKonohanaDownUp = function()
    CH("KONOHANA"):SetMotion(SymMot("EV001_DOWN02"), LOOP.OFF)
    CH("KONOHANA"):WaitPlayMotion()
    CH("KONOHANA"):ResetFacialMotion()
    subEveLookAround02(CH("KONOHANA"), Speed(600))
    CH("KONOHANA"):DirTo(CH("HERO"), Speed(500), ROT_TYPE.NEAR)
    CH("KONOHANA"):WaitRotate()
  end
  SOUND:FadeInEnv(SymSnd("SE_ENV_BREEZE_LP"), TimeSec(0.5), Volume(256))
  SCREEN_A:FadeIn(TimeSec(0.5), true)
  TASK:Sleep(TimeSec(1))
  CAMERA:SetEye(SymCam("CAMERA_02"))
  CAMERA:SetTgt(SymCam("CAMERA_02"))
  TASK:Regist(Group("hero"), taskHeroDownUp)
  TASK:Sleep(TimeSec(0.2))
  TASK:Regist(Group("konohana"), taskKonohanaDownUp)
  TASK:WaitTask(Group("konohana"))
  WINDOW:DrawFace(324, 88, SymAct("KONOHANA"), FACE_TYPE.PAIN)
  WINDOW:Talk(SymAct("KONOHANA"), 786168626)
  WINDOW:KeyWait()
  SOUND:PlaySe(SymSnd("SE_EVT_JUMP_02"), Volume(256))
  TASK:Regist(subEveDoubleJump, {
    CH("KONOHANA")
  })
  WINDOW:DrawFace(324, 88, SymAct("KONOHANA"), FACE_TYPE.ANGRY)
  WINDOW:Talk(SymAct("KONOHANA"), 100051185)
  WINDOW:Talk(SymAct("KONOHANA"), 485333424)
  WINDOW:CloseMessage()
  SOUND:PlaySe(SymSnd("SE_EVT_FOOT_STEP_01_LP"))
  CH("KONOHANA"):RunTo(SymPos("P01_KONOHANA"), Speed(4))
  CH("HERO"):RunTo(SymPos("P01_HERO"), Speed(4))
  CAMERA:SetEye(SymCam("CAMERA_01"))
  CAMERA:SetTgt(SymCam("CAMERA_01"))
  TASK:Sleep(TimeSec(1))
  SOUND:FadeOutEnv(TimeSec(1.5))
  SOUND:FadeOutSe(SymSnd("SE_EVT_FOOT_STEP_01_LP"), TimeSec(0.7))
  SCREEN_A:FadeOut(TimeSec(0.5), true)
end
