function main22_shinnyuusha01_start()
  CAMERA:SetSisaAzimuthDifferenceVolume(Volume(2))
  CAMERA:SetSisaRateVolume(Volume(0.5))
  CAMERA:SetEye(SymCam("CAMERA_00"))
  CAMERA:SetTgt(SymCam("CAMERA_00"))
  local taskHero00 = function()
    CH("HERO"):RunTo(SymPos("P00_HERO"), Speed(3))
    CH("HERO"):WaitMove()
    SOUND:PlaySe(SymSnd("SE_EVT_SIGN_SHOCK_02"), Volume(256))
    CH("HERO"):SetManpu("MP_EXCLAMATION")
  end
  TASK:Regist(taskHero00)
  local taskNyasupaa00 = function()
    CH("NYASUPAA"):RunTo(SymPos("P00_NYASUPAA"), Speed(3))
    CH("NYASUPAA"):WaitMove()
    CH("NYASUPAA"):SetManpu("MP_EXCLAMATION")
  end
  TASK:Regist(taskNyasupaa00)
  SCREEN_A:FadeIn(TimeSec(0.5), true)
  TASK:WaitTask()
  TASK:Sleep(TimeSec(0.2))
  CH("OOBEMU"):DirTo(RotateTarget(0), Speed(350), ROT_TYPE.NEAR)
  CH("OOBEMU"):WaitRotate()
  EFFECT:Create("effectBgConLp", SymEff("BG_CONCENTRATION_LP"))
  EFFECT:SetPosition("effectBgConLp", CH("OOBEMU"), BODY_POINT.CENTER)
  EFFECT:Play("effectBgConLp")
  SOUND:PlaySe(SymSnd("SE_EVT_DEDEEN"), Volume(256))
  CAMERA:SetEye(SymCam("CAMERA_01"))
  CAMERA:SetTgt(SymCam("CAMERA_01"))
  CAMERA:MoveFollow2(SymCam("CAMERA_01_5"), Speed(7, ACCEL_TYPE.NONE, DECEL_TYPE.HIGH))
  CAMERA:WaitMove()
  WINDOW:DrawFace(20, 88, SymAct("HERO"), FACE_TYPE.SURPRISE)
  WINDOW:Monologue(246852124)
  SOUND:WaitSe(SymSnd("SE_EVT_DEDEEN"))
  WINDOW:CloseMessage()
  EFFECT:Remove("effectBgConLp")
  SOUND:PlayBgm(SymSnd("BGM_EVE_FEAR_01"), Volume(256))
  CAMERA:SetEye(SymCam("CAMERA_02"))
  CAMERA:SetTgt(SymCam("CAMERA_02"))
  SOUND:PlaySe(SymSnd("SE_EVT_SIGN_NOTICE_LOW_01"), Volume(256))
  CH("NYASUPAA"):SetManpu("MP_EXCLAMATION")
  CH("NYASUPAA"):WaitManpu()
  WINDOW:DrawFace(20, 88, SymAct("NYASUPAA"), FACE_TYPE.SURPRISE)
  WINDOW:Talk(SymAct("NYASUPAA"), 397253469)
  WINDOW:CloseMessage()
  CH("OOBEMU"):SetMotion(SymMot("BATTLE"), LOOP.ON)
  TASK:Sleep(TimeSec(0.3))
  SOUND:PlaySe(SymSnd("SE_EVT_FLUSH"), Volume(256))
  subEveFlash()
  CAMERA:SetEye(SymCam("CAMERA_03"))
  CAMERA:SetTgt(SymCam("CAMERA_03"))
  CAMERA:SetFovy(SymCam("CAMERA_03"))
  CH("HERO"):SetMotion(SymMot("BATTLE"), LOOP.ON)
  TASK:Sleep(TimeSec(0.2))
  CH("NYASUPAA"):SetMotion(SymMot("BATTLE"), LOOP.ON)
  WINDOW:DrawFace(324, 88, SymAct("HERO"), FACE_TYPE.DECIDE)
  WINDOW:Monologue(1015072926)
  WINDOW:Monologue(630979039)
  WINDOW:CloseMessage()
  EFFECT:Create("effectBgConLp", SymEff("BG_CONCENTRATION_LP"))
  EFFECT:SetPosition("effectBgConLp", CH("OOBEMU"), BODY_POINT.CENTER)
  EFFECT:Play("effectBgConLp")
  CAMERA:SetEye(SymCam("CAMERA_04"))
  CAMERA:SetTgt(SymCam("CAMERA_04"))
  CAMERA:SetFovy(SymCam("CAMERA_04"))
  CAMERA:MoveFollow2(SymCam("CAMERA_04_5"), Speed(9, ACCEL_TYPE.NONE, DECEL_TYPE.HIGH))
  local taskOobemu00 = function()
    CH("OOBEMU"):SetMotion(SymMot("CHARGE"), LOOP.OFF)
    CH("OOBEMU"):WaitPlayMotion()
    CH("OOBEMU"):SetMotion(SymMot("BATTLE"), LOOP.ON)
  end
  TASK:Regist(taskOobemu00)
  WINDOW:Talk(SymAct("OOBEMU"), 1792700184)
  CAMERA:WaitMove()
  TASK:WaitTask()
  WINDOW:CloseMessage()
  EFFECT:Remove("effectBgConLp")
  CAMERA:SetEye(SymCam("CAMERA_02"))
  CAMERA:SetTgt(SymCam("CAMERA_02"))
  TASK:Sleep(TimeSec(0.2))
  subEveFlash2()
  SOUND:FadeOutBgm(TimeSec(0.5))
  SOUND:PlaySe(SymSnd("SE_EVT_BOSS_WIPE"), Volume(256))
  EFFECT:BossWipe(TimeSec(60, TIME_TYPE.FRAME), true)
  CAMERA:SetDefaultGroundFovy()
  SYSTEM:SetNextDungeonBanner(false)
end
