      CH("SUPIAA"):DirTo(RotateOffs(-45), Speed(600))
      CH("SUPIAA"):WaitRotate()
      TASK:Sleep(TimeSec(3))
    end
    function TaskL.Finish()
      return
    end
  end
  local taskJibakoiru00 = function()
    function TaskL.Loop()
      CH("JIBAKOIRU"):DirTo(RotateOffs(-45), Speed(100))
      CH("JIBAKOIRU"):WaitRotate()
      TASK:Sleep(TimeSec(1.5))
      CH("JIBAKOIRU"):DirTo(RotateOffs(90), Speed(200))
      CH("JIBAKOIRU"):WaitRotate()
      TASK:Sleep(TimeSec(1.5))
      CH("JIBAKOIRU"):DirTo(RotateOffs(-45), Speed(100))
      CH("JIBAKOIRU"):WaitRotate()
      TASK:Sleep(TimeSec(5))
    end
    function TaskL.Finish()
      return
    end
  end
  SOUND:PlayBgm(SymSnd("BGM_EVE_CHUKEI"), Volume(256))
  SCREEN_A:FadeIn(TimeSec(0.5), false)
  TASK:Sleep(TimeSec(1))
  TASK:Regist(taskJibakoiru00)
  TASK:Sleep(TimeSec(3))
  CAMERA:MoveFollow2(SymCam("CAMERA_01"), Speed(4, ACCEL_TYPE.NONE, DECEL_TYPE.HIGH))
  CAMERA:WaitMove()
  TASK:Regist(taskNyorobon00)
  TASK:Regist(taskSupiaa00)
  SOUND:PlaySe(SymSnd("SE_EVT_SIGN_SHOCK_02"), Volume(256))
  CH("KONOHANA"):SetManpu("MP_SHOCK_R")
  CH("KONOHANA"):WaitManpu()
  WINDOW:DrawFace(324, 88, SymAct("KONOHANA"), FACE_TYPE.SURPRISE)
  WINDOW:Talk(SymAct("KONOHANA"), -1274488980)
  WINDOW:CloseMessage()
  SOUND:PlaySe(SymSnd("SE_EVT_SIGN_HURRY"), Volume(256))
  CH("PARTNER"):SetManpu("MP_FLY_SWEAT")
  CH("PARTNER"):WaitManpu()
  WINDOW:DrawFace(20, 88, SymAct("PARTNER"), FACE_TYPE.SURPRISE)
  WINDOW:SwitchTalk({PARTNER_0 = -1391204819, PARTNER_1 = 713816802})
  WINDOW:CloseMessage()
  CH("KONOHANA"):DirTo(CH("PARTNER"), Speed(350), ROT_TYPE.NEAR)
  TASK:Sleep(TimeSec(0.3))
  CH("PARTNER"):DirTo(CH("KONOHANA"), Speed(350), ROT_TYPE.NEAR)
  TASK:Sleep(TimeSec(0.2))
  CH("HERO"):DirTo(CH("KONOHANA"), Speed(350), ROT_TYPE.NEAR)
  CH("KONOHANA"):WaitRotate()
  WINDOW:DrawFace(324, 88, SymAct("KONOHANA"), FACE_TYPE.SAD)
  WINDOW:Talk(SymAct("KONOHANA"), 865127331)
