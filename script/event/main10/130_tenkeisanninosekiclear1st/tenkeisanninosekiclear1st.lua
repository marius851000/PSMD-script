      CH("NYOROBON"):DirTo(RotateOffs(90), Speed(300))
      CH("NYOROBON"):WaitRotate()
      TASK:Sleep(TimeSec(5))
      CH("NYOROBON"):DirTo(RotateOffs(-45), Speed(200))
      CH("NYOROBON"):WaitRotate()
      TASK:Sleep(TimeSec(5))
    end
