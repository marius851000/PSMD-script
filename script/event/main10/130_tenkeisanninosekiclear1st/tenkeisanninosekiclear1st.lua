  subEveCloseMsg()
  WINDOW:DrawFace(324, 88, SymAct("KONOHANA"), FACE_TYPE.SAD)
  WINDOW:Talk(SymAct("KONOHANA"), -528373277)
  WINDOW:Talk(SymAct("KONOHANA"), -107308894)
  WINDOW:CloseMessage()
