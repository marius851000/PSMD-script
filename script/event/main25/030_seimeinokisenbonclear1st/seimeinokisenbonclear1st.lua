function main25_seimeinokisenbonclear1st05_start()
  CAMERA:SetSisaAzimuthDifferenceVolume(Volume(3))
  CAMERA:SetSisaRateVolume(Volume(0.5))
  CAMERA:SetEye(SymCam("CAMERA_00"))
  CAMERA:SetTgt(SymCam("CAMERA_00"))
  CH("HERO"):RunTo(SymPos("P00_HERO"), Speed(3))
  CH("PARTNER"):RunTo(SymPos("P00_PARTNER"), Speed(3))
  SOUND:FadeInEnv(SymSnd("SE_EVT_EARTHQUAKE_M_LP"), TimeSec(0.5), Volume(32))
  SCREEN_A:FadeIn(TimeSec(0.5), true)
  local taskLookAt00 = function()
    CH("HERO"):SetManpu("MP_EXCLAMATION")
    CH("HERO"):SetMotion(SymMot("ENDURE"), LOOP.ON)
    TASK:Sleep(TimeSec(0.1))
    SOUND:PlaySe(SymSnd("SE_EVT_SIGN_SHOCK_02"), Volume(256))
    CH("PARTNER"):SetManpu("MP_SHOCK_R")
    CH("PARTNER"):SetMotion(SymMot("ENDURE"), LOOP.ON)
  end
  TASK:Regist(taskLookAt00)
  CAMERA:SetEye(SymCam("CAMERA_01"))
  CAMERA:SetTgt(SymCam("CAMERA_01"))
  EFFECT:StartMotionBlur(0, 0, 0)
  EFFECT:ChangeMotionBlurLevel(0.04, TimeSec(0.2))
  EFFECT:ChangeMotionBlurAlpha(0.5, TimeSec(0.2))
