function main07_konohanaiewoakeru01_start()
  CAMERA:SetSisaAzimuthDifferenceVolume(Volume(3))
  CAMERA:SetSisaRateVolume(Volume(0.5))
  WINDOW:Narration(TimeSec(0.5), TimeSec(0.5), -685392928)
  WINDOW:CloseMessage()
  CAMERA:SetEye(SymCam("CAMERA_00"))
  CAMERA:SetTgt(SymCam("CAMERA_00"))
  CH("HERO"):SetDir(RotateTarget(-45))
  CH("HERO"):SetMotion(SymMot("EV001_SLEEP01"), LOOP.ON, TimeSec(0))
  local taskWakeUpHero = function()
    CH("HERO"):SetMotion(SymMot("EV001_SLEEP02"), LOOP.OFF)
    CH("HERO"):WaitPlayMotion()
    CH("HERO"):SetMotion(SymMot("WAIT02"), LOOP.ON)
  end
  SOUND:PlayBgm(SymSnd("BGM_EVE_KONOHANANOIE"), Volume(256))
  SCREEN_A:FadeIn(TimeSec(0.5), true)
  TASK:Sleep(TimeSec(0.5))
  TASK:Regist(Group("grpHero"), taskWakeUpHero)
  TASK:WaitTask(Group("grpHero"))
  TASK:Sleep(TimeSec(0.3))
  WINDOW:Talk(SymAct("KONOHANA"), -834762079)
  SOUND:PlaySe(SymSnd("SE_EVT_SIGN_NOTICE_LOW_02"), Volume(256))
  CH("HERO"):SetManpu("MP_NOTICE_L")
  CH("HERO"):WaitManpu()
  CH("HERO"):DirTo(SymPos("P00_KONOHANA"), Speed(350), ROT_TYPE.NEAR)
  WINDOW:Talk(SymAct("KONOHANA"), -451683998)
  WINDOW:CloseMessage()
  CH("KONOHANA"):WalkTo(SymPos("P00_KONOHANA"), Speed(1.5))
  CH("KONOHANA"):WaitMove()
  WINDOW:DrawFace(20, 88, SymAct("KONOHANA"), FACE_TYPE.SAD)
  WINDOW:Talk(SymAct("KONOHANA"), -66525149)
  WINDOW:CloseMessage()
  SOUND:PlaySe(SymSnd("SE_EVT_SIGN_HURRY"), Volume(256))
  CH("KONOHANA"):SetManpu("MP_FLY_SWEAT")
  CH("KONOHANA"):WaitManpu()
  WINDOW:DrawFace(20, 88, SymAct("KONOHANA"), FACE_TYPE.SAD)
  WINDOW:Talk(SymAct("KONOHANA"), -1287029020)
  WINDOW:Talk(SymAct("KONOHANA"), -1437446235)
  WINDOW:Talk(SymAct("KONOHANA"), -2122376090)
  WINDOW:CloseMessage()
  subEveNod(CH("HERO"))
  TASK:Sleep(TimeSec(0.2))
  CH("KONOHANA"):SetFacialMotion(FACIAL_MOTION.HAPPY)
  CH("KONOHANA"):SetManpu("MP_LAUGH_LP")
  WINDOW:DrawFace(20, 88, SymAct("KONOHANA"), FACE_TYPE.HAPPY)
  WINDOW:Talk(SymAct("KONOHANA"), -1738265305)
  WINDOW:KeyWait()
  CH("KONOHANA"):ResetFacialMotion()
  CH("KONOHANA"):ResetManpu()
  WINDOW:DrawFace(20, 88, SymAct("KONOHANA"), FACE_TYPE.NORMAL)
  WINDOW:Talk(SymAct("KONOHANA"), 536621544)
  WINDOW:Talk(SymAct("KONOHANA"), 115802281)
  WINDOW:Talk(SymAct("KONOHANA"), 619580836)
  WINDOW:Talk(SymAct("KONOHANA"), 1039482085)
  WINDOW:CloseMessage()
  TASK:Regist(subEveMoveDir2, {
    CH("KONOHANA"),
    SymPos("P01_KONOHANA"),
    Speed(1.5),
    SymPos("P01_KONOHANA")
  })
  TASK:Sleep(TimeSec(1.5))
  SOUND:FadeOutBgm(TimeSec(0.5))
  SCREEN_A:FadeOut(TimeSec(0.5), true)
end
