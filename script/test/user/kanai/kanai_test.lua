  SCREEN_A:WaitFade()
  TASK:Sleep(TimeSec(3))
  SCREEN_A:ChangeColorFilter(Color(1, 0, 0, 0), TimeSec(1), false)
  SCREEN_A:WaitFade()
  TASK:Sleep(TimeSec(3))
  SCREEN_A:SetColorFilter(Color(0, 0, 0, 0))
  SCREEN_A:FadeChange(Level(0), Level(0.5), TimeSec(1), false)
  SCREEN_A:WaitFade()
  TASK:Sleep(TimeSec(3))
  SCREEN_A:FadeChange(Level(0.5), Level(0), TimeSec(1), false)
  SCREEN_A:WaitFade()
  TASK:Sleep(TimeSec(3))
  SCREEN_A:WhiteChange(Level(0), Level(0.5), TimeSec(1), false)
  SCREEN_A:WaitFade()
  TASK:Sleep(TimeSec(3))
  SCREEN_A:WhiteChange(Level(0.5), Level(0), TimeSec(1), false)
  SCREEN_A:WaitFade()
  TASK:Sleep(TimeSec(3))
  SCREEN_A:WhiteOut(TimeSec(1), true)
  SCREEN_A:SetColorFilter(Color(0, 0, 0, 1))
  SCREEN_A:WhiteIn(TimeSec(1), true)
  SCREEN_A:WhiteOut(TimeSec(1), true)
  SCREEN_A:SetColorFilter(Color(0, 0, 0, 0))
  SCREEN_A:WhiteIn(TimeSec(1), true)
  EFFECT:Create("fire0", SymEff("DR_FIRE_LP"))
  EFFECT:Create("fire1", SymEff("DR_FIRE_LP"))
  EFFECT:Create("fire2", SymEff("DR_FIRE_LP"))
