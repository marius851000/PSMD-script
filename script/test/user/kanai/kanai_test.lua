function groundInit()
  CAMERA:SetEye(SymCam("Camera01"))
  CAMERA:SetTgt(SymCam("Camera01"))
end
function groundStart()
  SYSTEM:ResetDebugTimer()
  SYSTEM:ShowDebugTimer(true)
  SCREEN_A:FadeIn(TimeSec(0), false)
  SCREEN_A:FadeInAll(TimeSec(0), false)
  MAP:SetFog(0, 100, Color(1, 0, 0, 1))
  WINDOW:SwitchSysMsg({PARTNER_0 = -1514196545, PARTNER_1 = -1130102530})
  WINDOW:SwitchNaration(TimeSec(1), TimeSec(1), {PARTNER_0 = -1752607939, PARTNER_1 = -1903009156})
  WINDOW:Talk(SymAct("???"), SYSTEM:GetItemColorSymbol(1))
  WINDOW:Talk(SymAct("???"), -1043073861)
  WINDOW:Monologue2(-657930758)
  Test00()
  MAP:ClearFog()
  GIMMICK:SetupLoopPlaced(SymGA("EV_460_DESERT_OBJ"), 14, 0)
  GIMMICK:MoveLoopPlaced(Speed(2))
  GIMMICK:ShutdownLoopPlaced()
end
function groundEnd()
  WINDOW:SystemLetter(-203057607)
end
