function SUBOMII(chSymb0, chSymb1, chSymb2)
  CH(chSymb0):DirTo(CH(chSymb1), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb0):WaitRotate()
  TASK:Sleep(TimeSec(0.3))
  CH(chSymb0):DirTo(CH(chSymb2), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb0):WaitRotate()
  CH(chSymb0):SetManpu("MP_QUESTION")
  CH(chSymb0):WaitManpu()
  WINDOW:DrawFace(20, 88, SymAct("SUBOMII"), FACE_TYPE.NORMAL)
  WINDOW:Talk(SymAct("SUBOMII"), 1647152763)
  subEveDoubleJump(CH(chSymb0))
  subEveCloseMsg()
  CH(chSymb1):SetManpu("MP_LAUGH_LP")
  WINDOW:DrawFace(20, 88, SymAct("PUPURIN"), FACE_TYPE.NORMAL)
  WINDOW:Talk(SymAct("PUPURIN"), -1587897636)
  subEveCloseMsg()
  CH(chSymb1):ResetManpu()
  subEveDoubleJump(CH(chSymb2))
  WINDOW:DrawFace(20, 88, SymAct("UPAA"), FACE_TYPE.NORMAL)
  WINDOW:Talk(SymAct("UPAA"), -1203655779)
  WINDOW:CloseMessage()
end
function PUPURIN(chSymb0, chSymb1, chSymb2)
