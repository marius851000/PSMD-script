function YANCHAMU(chSymb0, chSymb1)
  CH(chSymb0):SetManpu("MP_ANGRY_LP")
  SOUND:PlaySe(SymSnd("SE_EVT_SIGN_ANGRY_02"), Volume(256))
  subEveDoubleJump(CH(chSymb0))
  CH(chSymb0):SetFacialMotion(FACIAL_MOTION.ANGRY)
  WINDOW:DrawFace(20, 88, SymAct("YANCHAMU"), FACE_TYPE.ANGRY)
  WINDOW:Talk(SymAct("YANCHAMU"), -257957981)
  WINDOW:CloseMessage()
  CH(chSymb0):DirTo(CH("PARTNER"), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb0):WaitRotate()
  TASK:Sleep(TimeSec(0.3))
  CH(chSymb0):SetFacialMotion(FACIAL_MOTION.SURPRISE)
  SOUND:PlaySe(SymSnd("SE_EVT_SIGN_SHOCK_02"), Volume(256))
  CH(chSymb0):SetManpu("MP_EXCLAMATION")
  TASK:Sleep(TimeSec(0.3))
  CH(chSymb0):DirTo(CH("HERO"), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb0):WaitRotate()
  CH(chSymb0):ResetManpu()
  WINDOW:DrawFace(20, 88, SymAct("YANCHAMU"), FACE_TYPE.SURPRISE)
  WINDOW:Talk(SymAct("YANCHAMU"), -377164062)
  WINDOW:CloseMessage()
  CH(chSymb0):ResetFacialMotion()
end
