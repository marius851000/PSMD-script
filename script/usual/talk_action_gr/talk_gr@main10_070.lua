function MIRUHOGGU(chSymb0, bFirst)
  if bFirst then
    WINDOW:DrawFace(20, 88, SymAct("MIRUHOGGU"), FACE_TYPE.THINK)
    CH(chSymb0):SetFacialMotion(FACIAL_MOTION.THINK)
    WINDOW:Talk(SymAct("MIRUHOGGU"), -428301511)
    WINDOW:Talk(SymAct("MIRUHOGGU"), 1642119158)
    WINDOW:DrawFace(20, 88, SymAct("MIRUHOGGU"), FACE_TYPE.SAD)
    CH(chSymb0):SetFacialMotion(FACIAL_MOTION.SAD)
    WINDOW:Talk(SymAct("MIRUHOGGU"), 2029751991)
    WINDOW:DrawFace(20, 88, SymAct("MIRUHOGGU"), FACE_TYPE.PAIN)
    CH(chSymb0):SetFacialMotion(FACIAL_MOTION.PAIN)
    WINDOW:Talk(SymAct("MIRUHOGGU"), -1148413424)
    WINDOW:Talk(SymAct("MIRUHOGGU"), -1567118511)
    WINDOW:CloseMessage()
    CH(chSymb0):SetMotion(SymMot("DAMAGE"), LOOP.OFF)
    CH(chSymb0):MoveTo(PosOffs(0, 0.3), Speed(2.5), LINK_DIR.OFF)
    CH(chSymb0):WaitMove()
    CH(chSymb0):SetMotion(SymMot("WAIT02"), LOOP.ON)
    WINDOW:DrawFace(20, 88, SymAct("MIRUHOGGU"), FACE_TYPE.SHOUT)
    CH(chSymb0):SetFacialMotion(FACIAL_MOTION.SHOUT)
    WINDOW:Talk(SymAct("MIRUHOGGU"), -1984235374)
    WINDOW:DrawFace(20, 88, SymAct("MIRUHOGGU"), FACE_TYPE.SPECIAL01)
    CH(chSymb0):ResetFacialMotion()
    WINDOW:Talk(SymAct("MIRUHOGGU"), -1868445229)
    WINDOW:Talk(SymAct("MIRUHOGGU"), -538943724)
    WINDOW:DrawFace(20, 88, SymAct("MIRUHOGGU"), FACE_TYPE.SPECIAL02)
    CH(chSymb0):SetFacialMotion(FACIAL_MOTION.HAPPY)
    CH("MIRUHOGGU"):SetMotion(SymMot("EV005_CONFUSE"), LOOP.ON)
    WINDOW:Talk(SymAct("MIRUHOGGU"), -956600747)
    WINDOW:CloseMessage()
    TASK:Sleep(TimeSec(0.5))
    CH("MIRUHOGGU"):SetMotion(SymMot("WAIT02"), LOOP.ON)
    CH(chSymb0):DirTo(CH("HERO"), Speed(350), ROT_TYPE.NEAR)
    CH(chSymb0):WaitRotate()
    SOUND:PlaySe(SymSnd("SE_EVT_SIGN_SHOCK_02"), Volume(256))
    CH(chSymb0):SetManpu("MP_SHOCK_L")
    CH(chSymb0):SetMotion(SymMot("DAMAGE"), LOOP.OFF)
    CH(chSymb0):WaitPlayMotion()
    CH(chSymb0):WaitManpu()
    WINDOW:DrawFace(20, 88, SymAct("MIRUHOGGU"), FACE_TYPE.SURPRISE)
    CH(chSymb0):SetFacialMotion(FACIAL_MOTION.SURPRISE)
    WINDOW:Talk(SymAct("MIRUHOGGU"), -304726634)
    WINDOW:Talk(SymAct("MIRUHOGGU"), -187888425)
    subEveDoubleJump(CH(chSymb0))
    WINDOW:CloseMessage()
    SOUND:PlaySe(SymSnd("SE_EVT_SIGN_HURRY"), Volume(256))
    CH(chSymb0):SetManpu("MP_FLY_SWEAT")
    CH(chSymb0):WaitManpu()
    WINDOW:DrawFace(20, 88, SymAct("MIRUHOGGU"), FACE_TYPE.SPECIAL02)
    CH(chSymb0):SetFacialMotion(FACIAL_MOTION.HAPPY)
    WINDOW:Talk(SymAct("MIRUHOGGU"), 1934954520)
    WINDOW:CloseMessage()
  else
    CH(chSymb0):DirTo(CH("HERO"), Speed(350), ROT_TYPE.NEAR)
    CH(chSymb0):WaitRotate()
    SOUND:PlaySe(SymSnd("SE_EVT_SIGN_HURRY"), Volume(256))
    CH(chSymb0):SetManpu("MP_FLY_SWEAT")
    CH(chSymb0):WaitManpu()
    WINDOW:DrawFace(20, 88, SymAct("MIRUHOGGU"), FACE_TYPE.SPECIAL02)
    WINDOW:Talk(SymAct("MIRUHOGGU"), 1783505241)
    WINDOW:CloseMessage()
  end
  CH(chSymb0):ResetFacialMotion()
end
