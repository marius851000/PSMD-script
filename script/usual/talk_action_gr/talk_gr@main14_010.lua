  WINDOW:Talk(SymAct("TANEBOO"), -866181842)
  subEveCloseMsg()
  CH(chSymb0):DirTo(RotateTarget(220), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb0):WaitRotate()
  CH("HERO"):DirTo(RotateTarget(220), Speed(350), ROT_TYPE.NEAR)
  CH("HERO"):WaitRotate()
  WINDOW:DrawFace(20, 88, SymAct("TANEBOO"), FACE_TYPE.NORMAL)
  WINDOW:Talk(SymAct("TANEBOO"), 858432841)
  WINDOW:CloseMessage()
  CH(chSymb1):DirTo(CH(chSymb0), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb1):WaitRotate()
  CH(chSymb0):DirTo(CH(chSymb1), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb0):WaitRotate()
