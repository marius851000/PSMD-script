function WARUBIARU(chSymb0, chSymb1)
  local degree0 = CH(chSymb0):GetDir()
  local degree1 = CH(chSymb1):GetDir()
  CH(chSymb0):DirTo(CH("HERO"), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb0):WaitRotate()
  TASK:Sleep(TimeSec(0.3))
  CH(chSymb0):SetManpu("MP_ANGRY_LP")
  SOUND:PlaySe(SymSnd("SE_EVT_SIGN_ANGRY_02"), Volume(256))
  CH(chSymb1):DirTo(CH("HERO"), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb0):WaitRotate()
  CH(chSymb1):SetFacialMotion(FACIAL_MOTION.SURPRISE)
  TASK:Sleep(TimeSec(0.5))
  WINDOW:DrawFace(20, 88, SymAct("WARUBIARU"), FACE_TYPE.ANGRY)
  CH(chSymb0):SetFacialMotion(FACIAL_MOTION.ANGRY)
  WINDOW:Talk(SymAct("WARUBIARU"), 18663371)
  CH(chSymb0):ResetManpu()
  TASK:Sleep(TimeSec(0.5))
  CH(chSymb0):ResetFacialMotion()
  CH(chSymb0):DirTo(RotateTarget(degree0), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb0):WaitRotate()
  WINDOW:KeyWait()
  SOUND:PlaySe(SymSnd("SE_EVT_SIGN_HURRY"), Volume(256))
  CH(chSymb1):SetManpu("MP_FLY_SWEAT")
  WINDOW:DrawFace(20, 88, SymAct("WARUBIRU"), FACE_TYPE.PAIN)
  CH(chSymb1):SetFacialMotion(FACIAL_MOTION.PAIN)
  WINDOW:Talk(SymAct("WARUBIRU"), 403175050)
  WINDOW:CloseMessage()
  CH(chSymb1):ResetFacialMotion()
  CH(chSymb1):DirTo(RotateTarget(degree0), Speed(350), ROT_TYPE.NEAR)
  CH(chSymb1):WaitRotate()
end
function WARUBIRU(chSymb0, chSymb1)
