function PARTNER(chSymb0)
  CH(chSymb0):SetFacialMotion(FACIAL_MOTION.SAD)
  WINDOW:DrawFace(20, 88, SymAct("PARTNER"), FACE_TYPE.SAD)
  WINDOW:SwitchTalk({PARTNER_0 = -2112273807, PARTNER_1 = -1694338256})
  WINDOW:SwitchTalk({PARTNER_0 = -1339088653, PARTNER_1 = -1456205390})
  WINDOW:KeyWait()
  CH(chSymb0):SetFacialMotion(FACIAL_MOTION.THINK)
  WINDOW:DrawFace(20, 88, SymAct("PARTNER"), FACE_TYPE.THINK)
  WINDOW:SwitchTalk({PARTNER_0 = -428505227, PARTNER_1 = -9521612})
  WINDOW:CloseMessage()
  CH(chSymb0):ResetFacialMotion()
end
