function YAMIKARASU(chSymb0)
  WINDOW:DrawFace(20, 88, SymAct("YAMIKARASU"), FACE_TYPE.HAPPY)
  CH(chSymb0):SetFacialMotion(FACIAL_MOTION.HAPPY)
  WINDOW:Talk(SymAct("YAMIKARASU"), 1568209220)
  WINDOW:CloseMessage()
  CH(chSymb0):ResetFacialMotion()
end
