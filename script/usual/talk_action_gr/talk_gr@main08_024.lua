end
function RATTA(chSymb0, chSymb1)
  WINDOW:DrawFace(20, 88, SymAct("RATTA"), FACE_TYPE.NORMAL)
  WINDOW:Talk(SymAct("RATTA"), -412977914)
  WINDOW:DrawFace(20, 88, SymAct("RATTA"), FACE_TYPE.THINK)
  WINDOW:Talk(SymAct("RATTA"), -25607097)
  WINDOW:CloseMessage()
  CH(chSymb1):DirTo(CH(chSymb0), Speed(350), ROT_TYPE.NEAR)
  SOUND:PlaySe(SymSnd("SE_EVT_JUMP_02"), Volume(256))
  subEveDoubleJump(CH(chSymb1))
  WINDOW:DrawFace(20, 88, SymAct("KORATTA"), FACE_TYPE.PAIN)
  WINDOW:Talk(SymAct("KORATTA"), -1321676160)
  WINDOW:CloseMessage()
  SOUND:PlaySe(SymSnd("SE_EVT_JUMP_02"), Volume(256))
  subEveDoubleJump(CH(chSymb1))
  WINDOW:DrawFace(20, 88, SymAct("KORATTA"), FACE_TYPE.PAIN)
  WINDOW:Talk(SymAct("KORATTA"), -1474042943)
  WINDOW:CloseMessage()
end
function KORATTA(chSymb0, chSymb1)
  RATTA(chSymb0, chSymb1)
end
